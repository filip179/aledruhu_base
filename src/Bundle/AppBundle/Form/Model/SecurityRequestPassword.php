<?php

namespace AppBundle\Form\Model;

use Component\Form\Model\FormModelInterface;

class SecurityRequestPassword implements FormModelInterface {
    private $email;

    /**
     * @return mixed
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param mixed $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }
}
